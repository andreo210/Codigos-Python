"""
Junte duas tuplas
Para juntar duas ou mais tuplas você pode usar o + operador:
"""
tuple1 = ("a", "b", "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)


"""
Multiplicar Tuplas
Se você quiser multiplicar o conteúdo de uma tupla
 um determinado número de vezes, você pode usar o * operador:
"""
fruits = ("apple", "banana", "cherry")
mytuple = fruits * 2

print(mytuple)