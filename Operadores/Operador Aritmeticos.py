a = 3
b = 5
# adição
print(a + 5)

# subtração
print(a - 5)

# divisão
print(a / 5)

# multiplicação
print(a * 5)

# exponenciasão
print(a ** 5)

# mod
print(a % 5)

# divisão inteira(sem o numero quebrado)
print(16 // 3)

# Parênteses têm a maior precedência, o que significa que as expressões
#  dentro deles devem ser avaliadas primeiro
print((6 + 3) - (6 + 3))

# A multiplicação *tem precedência maior que a adição +e, portanto,
#  as multiplicações são avaliadas antes das adições:
print(100 + 5 * 3)
